# 写一个函数，打印一个整数的阶乘，N! = 1*2*3*...*N
def jie_cheng(num):
    sum = 1
    for x in range(1,num+1):
        sum *= x
    print('%d的阶乘为：%d'%(num,sum))

num1 = int(input('请输入一个数：'))
jie_cheng(num1)
